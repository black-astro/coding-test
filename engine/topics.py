_DEFAULT = "구현/시뮬레이션"


def classify(p) -> str:
    text = ((getattr(p, "topic", "") or "") + " " + (getattr(p, "category", "") or "")
            + " " + (getattr(p, "title", "") or "")).lower()
    for label, kws in _RULES:
        for kw in kws:
            if kw.lower() in text:
                return label
    return _DEFAULT


def build(all_problems) -> dict:
    groups = {t: [] for t in TOPIC_ORDER}
    for p in all_problems:
        groups.setdefault(classify(p), []).append(p)
    for t, lst in groups.items():
        lst.sort(key=lambda p: (profile.problem_points(p), p.id))
    return groups
